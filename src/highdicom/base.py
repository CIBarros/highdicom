class SOPClass(Dataset):

    def copy_attributes_from(self, dataset: Dataset) -> None:
        # Copy all patient and study level attributes from one of the
        # referenced data sets.
        logger.debug(
            'copy attributes from dataset "{}"'.format(dataset.SOPInstanceUID)
        )
        for tag in _ATTRIBUTES_TO_COPY:
            keyword = keyword_for_tag(tag)
            logger.debug('copy attribute "{}"'.format(keyword))
            try:
                data_element = dataset[tag]
            except KeyError:
                continue
            self.add(data_element)
